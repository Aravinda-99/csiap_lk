    <p>Irrigation rehabilitation programs are implemented for the rehabilitation, modernization of tanks in the hotspot area. This rehabilitation includes restoration of vegetations around the tanks to minimize water evaporation and absorption of metals in recycled water and desilting. The restoration process was successfully achieved in North Central province replanting 300 trees saving 48 trees during the tank rehabilitation process. &nbsp;The CSIAP was able to save thousands of trees as well as flora and fauna during the implementation of project activities including ASC modernization, tank rehabilitation, agri road rehabilitating and cultivation program. &nbsp;&nbsp;</p>
    <p>The Human-Elephant Conflict (HEC) is particularly significant in all the Hotspot Areas. To mitigate HEC in the hotspot area, the project has planned a mechanism to build a direct collision with humans and elephants. In order to avoid further intensifying HEC, innovative landscape-level strategies and 410 km extent &ldquo;<strong>Bio Fencing</strong>&rdquo; will be established where wildlife and humans can co-exist in the same landscape.</p>
    <p>&nbsp;A decade on Ecosystem Restoration 2021-2030 will be declared by the United Nations to Reimagine, Recreate and restore the ecosystem in the world this year. Similar to the UN Decade on Ecosystem Restoration 2021-2030, the CSIAP has planned to implement the below-mentioned programs to reimagine, recreate and restore the ecosystem in the hotspot area.</p>
    <p><strong>Model Cascade Development -</strong> Project will develop eleven models Cascade to</p>
    <p>1. &nbsp; &nbsp; Holding the water from monsoons and preventing flood, recharging groundwater and storing water for irrigation, household and other uses</p>
    <p>2. &nbsp; &nbsp;Support restoration of vegetations around the tanks and</p>
    <p>3. &nbsp; &nbsp;Minimizing ecological and offsite impacts done by the farmers themselves in HAS. The aim is to enable communities to better cope with climate change impacts while restoration of ecosystem</p>
    <p><strong>Agro-ecological Landscape Resilience Plans -&nbsp;</strong>The project proposes to develop a set of science-based &quot;integrated&quot; climate-resilient agro-ecological landscape plan focusing on the selected cascade of the HSA. This is to understand the biodiversity attributes, ecosystem services and to introduce Nature base Solutions (NbS) for the current issues and seek opportunities to bring in greater environmental and social resilience through appropriate.</p>
