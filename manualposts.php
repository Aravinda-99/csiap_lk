    <p>&nbsp;The CSIAP is expected to bring positive environmental benefits to the project areas through the scale-up of climate-resilient agricultural technologies and farming practices that help improve soil health, water use efficiency, and catchment area treatment to promote more efficient use of surface water and more sustainable use of groundwater for agriculture. An Environment Assessment and Management Framework (EAMF) has been prepared to guide the screening of activities for physical investments, project-supported advisory, policy support interventions, resulting in implementation from an environmental perspective, mitigation actions to manage their environmental impacts including preparation, implementation of Environmental Assessments (EAs) and Environmental Management Plans (EMPs) to address site-specific risks and impacts and subsequent monitoring and reporting requirements.</p>
    <p>The CSIAP has taken several actions to&nbsp;bring awareness among the people regarding environmental issues and to take positive steps for the survival of the environment and ecosystem restoration. More than 150 Environmental Screening and Environmental Management Plans were prepared to identify the negative environmental impact and to produce mitigation measures through the project activities. The Green Building Concept was introduced through the 47 ASC modernization activities. It highly emphasized asbestos and led free materials for the use of the rehabilitation process.</p>
    <p>The project has conducted five major production programs which were applied a holistic pest control approach that utilizes all available pest control strategies on a case-by-case basis while maintaining the pest population below at an Economically Injury Level (EIL). Whenever possible, human-animal and ecosystem-friendly pest control strategies are used.</p>
