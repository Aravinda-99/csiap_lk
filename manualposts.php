<blockquote>
    <p><strong>Recognizing rural women as &lsquo;farmers&rsquo; and as &lsquo;leaders&rsquo;in adopting climate resilience agriculture technology and practices</strong></p>
</blockquote>
<div><a href="https://1.bp.blogspot.com/-TRpT8mQp4NA/X4smiJmYJHI/AAAAAAAAArc/RHx8xMzdoBAt2-k01Lay5ZoGFm8Nz0K3wCLcBGAsYHQ/s1844/DSCF4806.jpg"><img border="0" height="360" src="https://1.bp.blogspot.com/-TRpT8mQp4NA/X4smiJmYJHI/AAAAAAAAArc/RHx8xMzdoBAt2-k01Lay5ZoGFm8Nz0K3wCLcBGAsYHQ/w640-h360/DSCF4806.jpg" width="640"></a></div>
<p>&nbsp;Sri Lanka is a lower-middle-income country with total population of 21.8 million of which 77.4% of the population is largely rural.<a href="file:///C%3A/Users/User/Desktop/New%20folder%2000/CSIAP%20Empowering%20Rural%20Women.docx#_ftn1" name="_ftnref1" title="">[1]</a> Overall, poverty level of the country is 6.7%, and poverty remains concentrated in rural areas - of the poor, 82 percent are rural residents.<a href="file:///C%3A/Users/User/Desktop/New%20folder%2000/CSIAP%20Empowering%20Rural%20Women.docx#_ftn2" name="_ftnref2" title="">[2]</a> While agriculture sector contributes 6.9 percent to GDP, the sector employs approximately 27% of the population, which is mostly rural..<a href="file:///C%3A/Users/User/Desktop/New%20folder%2000/CSIAP%20Empowering%20Rural%20Women.docx#_ftn3" name="_ftnref3" title="">[3]</a> While the share of women in agriculture has remained between 30-33% for over two and a half decades<a href="file:///C%3A/Users/User/Desktop/New%20folder%2000/CSIAP%20Empowering%20Rural%20Women.docx#_ftn4" name="_ftnref4" title="">[4]</a>, women play a key role in agricultural production and processing, both in family and farms outside; though women&rsquo;s role have been mostly limited to home gardening and small-scale farming activities.</p>
<blockquote>
    <p dir="rtl">&nbsp;The World Bank funded &ldquo;Climate Smart irrigated Agriculture Project (CSIAP)&rdquo; targets farmers in climatically vulnerable hotpot areas to improve the productivity and resilience of smallholder agriculture activities.&nbsp;&nbsp;As part of the project&rsquo;s strategic and operational design, it seeks to address gender gaps and risks in order to empower women as &lsquo;farmers&rsquo; and as &lsquo;leaders&rsquo; in their communities for adopting climate resilience practices.</p>
</blockquote>
<div><strong>Rural women in farming communities face multiple challenges,&nbsp;</strong></div>
<div><strong>which risks them of being excluded from benefiting from project activities &nbsp;</strong></div>
<p>During the project preparation and assessment phase, many issues were identified which limits and prevents women from equitably benefiting from and participating in agriculture activities. These issues include: lack of land and asset ownership which limits women&rsquo;s ability to engage and grow their agricultural production and livelihoods (e.g. in Sri Lanka, only 16 % of all privately-owned land belongs to women)<a href="file:///C%3A/Users/User/Desktop/New%20folder%2000/CSIAP%20Empowering%20Rural%20Women.docx#_ftn1" name="_ftnref1" title="">[1]</a>, lack of access to climate smart agriculture production technologies and marketing, lack of decision-making power as most of the farmer organizations are led by male farmers, inability to fully participate in agriculture activities due&nbsp;household and care responsibilities, additional requirements to hire labour, difficulties in traveling to agriculture lands which are generally away from residential locations, greater risks of impact from the Human elephant conflict (HEC) as women mostly engage in cultivation work during day time when HEC is most prevalent and impacts linked to domestic abuse/gender based violence with increased consumption of alcohol by men.&nbsp;With all these setbacks for rural women, the current COVID pandemic situation increases their vulnerability affecting household income, food security and nutrition.&nbsp;</p>
<div><strong><br>Rural women benefited from gender informed approaches&nbsp;</strong><strong>in climate smart sub projects</strong>.</div>
<p>The project has taken multiplicity of actions to ensure the inclusion rural women in project activities. Thus the project has taken measures to:&nbsp;&nbsp;&nbsp;</p>
<div><a href="https://1.bp.blogspot.com/-rC1rBzOE62Y/X4sp_YayzII/AAAAAAAAAsU/lipLbeAtWxk7aAtr7JXvmBkkB7mgQmHbgCLcBGAsYHQ/s1844/DSCF5082.jpg"><img border="0" height="360" src="https://1.bp.blogspot.com/-rC1rBzOE62Y/X4sp_YayzII/AAAAAAAAAsU/lipLbeAtWxk7aAtr7JXvmBkkB7mgQmHbgCLcBGAsYHQ/w640-h360/DSCF5082.jpg" width="640"></a></div>
<div>
    <p>1.&nbsp; &nbsp; &nbsp;reformulate project strategies during COVID19 period to implement several subprojects specially targeting rural women farmers to ensure their food and nutrition security.</p>
    <p>2.&nbsp; &nbsp;&nbsp;assess gender gaps, issues and risks during participatory rural needs assessments and social screening. &nbsp;</p>
    <p>3.&nbsp; &nbsp;&nbsp;incorporate measures in hot-spot area development plans and social impact management plans to ensure women&rsquo;s participation and inclusion.</p>
    <p>4.&nbsp; &nbsp;&nbsp;revise beneficiary selection criteria to be inclusive giving priority to women beneficiaries including vulnerable groups (such as women who are living under the poverty level, widows, families with young children and those living with elderly &amp; disable persons)</p>
    <p>5.&nbsp; &nbsp; &nbsp;establish minimum targets for female beneficiaries in project interventions</p>
    <p>6.&nbsp; &nbsp;&nbsp;build capacities of project staff on gender issues and sensitize communities (both males and females) on principals of gender equity in line with the training manual developed.</p>
    <p>7.&nbsp; &nbsp; &nbsp;build capacities of women on climate-resilient agricultural technologies</p>
    <p>8.&nbsp; &nbsp;&nbsp;support to address risks of Human-Elephant conflict</p>
    <p>9.&nbsp; &nbsp;&nbsp;provide support to purchases assets, hire labour and access services (such as credit, childcare facilities, etc.)</p>
    <p>10.&nbsp;strengthen links with community-based organizations to receive project implementation support&nbsp;</p>
    <p>11.&nbsp;&nbsp;implement GBV prevention measures (i.e. Code of Conduct for Communities etc.) and strengthen GRM reporting measures for GBV.</p>
    <p>12.&nbsp;increase women&apos;s engagement in decision making in Farmer&rsquo;s Organizations, Producer&rsquo;s Groups &amp; Social Audit Committees including acting on issues affecting them&nbsp;</p>
    <p><strong>Over 5,500 rural women farmers involved climate resilience agriculture practices</strong></p>
    <div><a href="https://1.bp.blogspot.com/-j2YqEmwSHAI/X4sqMbHMEJI/AAAAAAAAAsY/HpYy98RX5UQxguy8sIAzhKzhNQO6dAyjQCLcBGAsYHQ/s1176/1.jpg"><img border="0" height="300" src="https://1.bp.blogspot.com/-j2YqEmwSHAI/X4sqMbHMEJI/AAAAAAAAAsY/HpYy98RX5UQxguy8sIAzhKzhNQO6dAyjQCLcBGAsYHQ/w400-h300/1.jpg" width="400"></a></div>
    <p>CSIAP strategies that are formulated and are being implemented have generated favorable outcomes for rural women farmers to ensure these groups are benefiting from climate resilience agriculture practices in an equitable manner. Therefore, in addition to increasing participation of women in project activities, the project has exceeded its target of 30% of beneficiaries to be women in all its programs and is also successfully implementing programs targeting 100% women as beneficiaries through its climate smart nutrition sensitive home gardening program.&nbsp;</p>
