                            <p style="margin-top: 50px;">Seed production is one of the most important activities under the Climate Samrat

Agriculture Programme. This program helps to produce and conserve traditional,



drought-tolerant &amp; short-age crop varieties. As a result of this program, it leads to

making available required seed varieties on time within the farm setting and this

helps to implement their cultivation with the onset of rains. Then, farmers can use

rainwater in a productive way which leads to increasing crop productivity without

facing any water deficiency. Hence, indirectly this practice helps to minimize fuel cost

for pumping irrigation and eventually reduce the Green House Gas (GHG) emission.

It is explicit, that the Climate Smart Seed Production Programme helps to increase

crop productivity by contributing to climate change adaptation and mitigation.</p>



<p>In the meantime, the project has carried out a seed production program in 2378 Ha

with 9158 farmers and produced 4162 M/tons of seed including green gram, Black

gram, Cowpea, Ground nut and Paddy seeds.</p>



                           



