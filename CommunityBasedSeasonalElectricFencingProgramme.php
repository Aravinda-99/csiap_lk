                            <p style="margin-top: 50px;">The Climate Smart Irrigated Agriculture Project (CSIAP) funded by the World Bank has
teamed up with the Centre for Conservation and Research (CCR) to install 261 mobile
elephant electric fencing or seasonal electric fencing, which could be deployed by
farmers at the start of cultivation and removed and stored after harvest, at a cost of Rs
475 million to mitigate the human-elephant conflict in the project area where the CSIAP
is being implemented.</p>







<p>In order to reduce the human-elephant-conflict in the project areas, theses electric
fences are installed in the area before starting the cultivation and it will be uninstalled
again after harvesting. The CSIAP emphasizes that the use of community-based
seasonal electric fencing has been shown to be very effective in safeguarding paddy
cultivations from elephant depredation.The human-elephant conflict is significant in all
the CSIAP’s hotspot areas because the cultivation in the hotspot area is often
threatened by wild animals, especially elephants.</p>


<p>This programme is being implemented in 08 districts of Anuradhapura, Kurunegala,
Puttalam, Kilinochchi, Mullaitivu, Trincomalee, Monaragala and Hambantota where the
CSIAP is being implemented. Under this project, 261 fences for 1109.3 km covering
77854.8 ha paddy fields in  128 Grama Niladhari Division areas of  34 Agrarian Service
Divisions. The estimated budget for this is Rs. 475 million and 18905 farming families
will benefit.</p>

<p>The CSIAP’s  Agro Ecological Landscape Resilience Plan (ALRP) has proposed
to study the seriousness of human-elephant conflict over the last 10 years. Particular
emphasis has been given on the number and intensity that exist in its landscape today,
and how it will increase and intensify with the agricultural expansion and productivity
improvement. Mapping of the potential movements of the elephants has suggested
mitigation measures. These maps would help identify the intensity of the conflict and the
places prone to the threat,   existing elephant corridors, and current elephant movement
patterns within the area. Further, the ALRP would study the people’s attitudes on
human-elephant conflict, traditional knowledge and practices on mitigation such
conflicts and evaluate the current status of the existing electric fences and  locations,
effectiveness, and gives a recommendation for their improvements</p>

<p>On behalf of the CSIAP, the Department of Agrarian Department (DAD) will implement
an erecting electric fencing programme and for this purpose, DAD has issued a circular
to establish an institutional mechanism.  The fences will be entirely powered by solar
energy. This project strives to assist farmers in the project area to protect their crops
from wild elephants that frequent the area – often causing heavy damage to paddy and
other crops. The Versatile Field Fences are easy to erect and the farmers are taught
how to make these by the CCR.
The fences stay up until the harvest is taken. Hen farmers remove them afterwards and
free up land for wild animals to eat. Again, erect fences for the next crop season. The
massive human-elephant conflict has now climbed to a high level, taking many human
lives and elephant deaths annually making Sri Lanka the highest annual elephant
deaths and second-highest human deaths due to HEC. In 2019, the human-elephant
conflict reached record heights in Sri Lanka with the death of 121 people and 405
elephants.
</p>

<p>With this project, the CSIAP hopes to minimize the destruction of crops by a significant
percentage and also diversify crops by the use of abandoned farmland due to elephant
crop raiding. The project will be implemented under the guidance of Dr. Prithviraj
Fernando and his team at the Centre for Conservation and Research. The programme
has several activities such as community awareness on maintenance of fencing,
protection of elephants and people and long-term coexistence. The CSIAP promotes
the man and elephant co-existence within hotspot areas while promoting environmental
conservation in the tank-based cascades.</p>







                           







