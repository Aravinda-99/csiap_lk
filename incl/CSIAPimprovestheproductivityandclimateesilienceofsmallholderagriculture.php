<div>Expressing his idea on CSIAP furthermore Mr. Sivalingam further added &ldquo;Sri Lanka is vulnerable to climate related natural disasters that have major economic impacts. The hotspot areas in the dry zone have emerged as being particularly susceptible to either flood or drought events, or both. Therefore, hotspot areas have been identified as the most vulnerable to climate events that affect the agriculture production and productivity.&rdquo;</div>
<div><br></div>
<div>&ldquo;Climate resilience is defined as the capacity of agriculture system to respond to drought and flood by resisting or tolerating the impacts and recovering quickly through integration of adaptation, mitigation and other climate smart agriculture practices. &nbsp;This project differs from the other projects by using the Participatory Rural Appraisal (PRA) to identify the problems, challenges and opportunities of smallholder agriculture communities in the hotspot area.&rdquo;</div>
<div><br></div>
<div>&nbsp;&ldquo;If we use the correct methods and also the active participation of all stakeholders during the PRA activity and also consultation with other stakeholders and partners, the issues and needs of the farmer communities can be fully understood and addressed through the project intervention. The information gathered from the PRA will be incorporated into a hot spot Area Agriculture Development Plan which will an action plan for the implementation of the project. Therefore, the HSAADP is very important to the CSIAP.&rdquo;</div>
<div><br></div>
<div>&ldquo;The HSAADP will also focus on the voice and needs of the socially excluded communities. These communities include women farmers, women farmer origination, women headed farm families physically challenge farmers poor smallholder farmers, farmers who are living under the poverty line, women who are living in the resettled areas or relocated communities, non-titleholders, ethnic minorities in the hotspot areas. Therefore, it is essential that the participation of these excluded communities in the PRA exercise and to count their voice and concerns.&rdquo;</div>
<div><br></div>
<div>&ldquo;Based on the HSAADP, sub projects will be identified taking into consideration of the important issues identified and the priorities placed by the communities we are going to identify the subprojects in terms of priority basis in the hotspot area. Therefore, the success of the project intervention and implementation definitely depends on the success of the HSAADP.&rdquo; &nbsp;</div>
<div><br></div>
