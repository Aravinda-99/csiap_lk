    The efficient way of preparing nursery for seedlings broadcasting&nbsp;was introduced to farmers in hotspot areas identified by Climate Smart Irrigated Agriculture Project (CSIAP). This method was practically demonstrated in Mahanikawewa and Ranorawa cascades in Anuradhapura District.<br>Seedlings broadcasting is a climate smart technology which is economically and environmentally friendly alternative option to seeding. This achieves what many are looking to accomplish: increase yields and quality, while decreasing costs, inputs and environmental impact.&nbsp;Prior to broadcasting, seedlings need to be raised in a nursery. In order to do this,&nbsp;pre-germinated seeds are sown on trays and seeds become seedlings there.<br>Main advantages of this nursery<div>1. &nbsp; &nbsp; Saving water needed for cultivation as seeds become seedlings separately</div>
        <div>2. &nbsp; &nbsp;It provides favourable growth conditions</div>
        <div>3. &nbsp; &nbsp;Better care of younger plants as it is easy to look after nursery in a small area against pathogenic infections, pests and diseases and weeds.</div>
        <div>4. &nbsp; &nbsp;Nursery helps in making more efficient use of land</div>
        <div>5. &nbsp; &nbsp; It helps to improve crop uniformity in the field</div>
        <div>6. &nbsp; &nbsp;Nursery helps to reduce field management cost</div>Mrs. Gourie Samaraweera, Deputy Director of Agriculture, explained and demonstrated how to prepare a nursery for seedlings broadcasting. Mr. Lakshman Premanath, Deputy Project Director, North Central Province, officials of the Provincial Department of Agriculture and CSIAP also present on the occasion.



