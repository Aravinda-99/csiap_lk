    The World Bank Board of Directors approved a $140 million credit for Sri Lanka to improve the resilience and productivity of agriculture for more than 470,000 small farmers in 6 provinces in the dry zone of the country under the Climate Smart Irrigated Agriculture Project (CSIAP). The provinces selected are those that are most exposed to climate impacts.&nbsp;The new CSIAP will be implemented by the Ministry of Agriculture, Rural Economic Affairs, Livestock Development, Irrigation and Fisheries &amp; Aquatic Resources along with the six Provincial Councils (Northern, North Central, North Western, Eastern, Uva and Southern) in the dry zone.<br><br>The total project cost is $140 million, including a $125 million credit from the International Development Association, with a $10 million contribution from the Government of Sri Lanka and a $5 million contribution from the project beneficiaries. Lanka is particularly vulnerable to climate-related natural disasters such as floods and droughts. The agriculture sector, which contributes approximately 7.7 percent to the country&rsquo;s economy and employs 27 percent of the population, more than 38 percent of whom are women; is especially<br>The objective of CSIAP for Sri Lanka is to improve the productivity and climate resilience of smallholder agriculture in selected hotspot areas. The project has four components.(1) Agriculture Production and Marketing component will improve agriculture productivity and diversification through the adoption of Climate Smart Agriculture (CSA) practices and improved on-farm water management.(2) Water for Agriculture component will facilitate (a) planning for water and other infrastructure necessary to support climate-resilient irrigated agriculture, (b) construction of the planned infrastructure, and (c) co-management of this infrastructure by central/provincial governments and the local community.(3) Project Management component will ensure the quality of overall project management, while ensuring smooth coordination of activity implementation by various agencies and strategic partners at national and subnational levels. This component will finance:(a) the consultancy and operating costs of the Project Management Unit (PMU) and Deputy Project Director (DPD) Offices and of different project executing agencies, including for fiduciary and safeguard aspects; (b) the monitoring and evaluation (M&amp;E) of project activities at baseline, midterm, and end of project, including geotagging of the assets created; and (c) information, education, and communication campaigns to make all stakeholders aware of the project.(4) Contingent Emergency Response component will allow for rapid reallocation of project proceeds in the event of a natural disaster or crisis that has caused or is likely to imminently cause a major adverse economic and social impact.</p>
