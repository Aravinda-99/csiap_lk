                            <p>The main objective of the modernization of ASCs is  to convert the community Centre as one-stop service Centre that caters the real needs of the farming community in the area. Following main areas have been identified as priority basis for the modernization of ASCs in all six provinces.</p>



                            <p>This is considered as one of the biggest sub-projects, which comes under the CSIAP. It has many components and therefore this project will involve in implementing following major interventions with a view to develop the ASC as a One-Stop Service Centre, which provides modern services to the farmers to become resilient to the changing climate situation and thereby to overcome the issues hinder their productivity. The major interventions are;</p>



                            <ul>

                                <li>Renovating, expanding and refurbishing existing 47 office buildings of the ASCs within areas of the CSIAP to function as an attractive and productive One-stop Service Centers</li>

                                <li>Establishing full pledged ICT unit within each of the 47 ASC with trained officials and necessary equipment to provide required data related to weather forecast, market, new technology for farmers under digitalization of ASC.</li>

                                <li>Establishing a Farm Machinery Hub (FMU) in each of the 47 ASC managed by the Agrarian Development Committee (ADC) of the ASC to prevent exploitation of farmers from the middlemen and also to provide an opportunity for the Agrarian Committee to develop the Agrarian Fund.</li>

                            </ul>



                            <p>The CSIAP has already begun the civil work of Modernization of ASCs. The Civil work of the Agrarian Service Centres has started as the first step of the modernization process.  All civil work has been divided into 11 packages in order to modernize 47 ASCs which has already being identified. The total civil cost of all the packages were Rs. 336.18 Mn</p>

