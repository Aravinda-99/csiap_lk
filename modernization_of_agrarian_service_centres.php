                            <p>These major interventions will be considered as components of the this project and each intervention will have a large number of activities to respond to the issues identified during the need assessment process conducted through the consultation meetings, visits to ASCs and the two workshops to develop the ASC. However, it is to be noted that to avoid practical difficulties, these interventions are implemented stage by stage. Therefore, considering the urgency, importance and the practicability, the Project Management Unit of the CSIAP has decided first to support the major intervention viz; the expansion or renovations/rehabilitation to the existing ASC. Accordingly, it has been identified that following major activities will involve in this first intervention.</p>



                            <ul>

                                <li>Renovating, repairing and refurbishing ASC for the effective operation to provide uninterrupted service to the farmers</li>

                                <li>Providing adequate space for Divisional level project t activities</li>

                                <li>Constructing and rehabilitating office space and resting facilities for the staff and the visiting staff from other departments.</li>

                                <li>Providing adequate space for E- unit for farmer data bank, data on marketing and business linkages, technology transfer</li>

                                <li>Providing space for legal advice and dispute resolution for farmers</li>

                                <li>Providing adequate storage facilities for inputs (seed paddy, fertilizers and cold room)</li>

                                <li>Establishing a place for demonstration and practices</li>

                                <li>Providing adequate space with equipment to conduct seminars, workshops etc. for the farmers.</li>

                                <li>Providing space for a soil testing unit</li>

                                <li>Constructing a proper vehicle park and garage</li>

