                            <p>Climate Smart Agriculture is a new concept to the Sri Lankan farming society, even though the farmers apply some traditional Climate Smart Agriculture (CSA) practices without a real scientific background. In this scenario, the project wishes to increase and build the capacities of farmers for the use of CSA technologies to combat the climate change impact. The project aims to build a village-level resource person pool with the competencies of CSA technologies & practices from support organization officials and selected leader farmers through the newly established CSFTS at Thirappane in Anuradhapura district.</p>

                            <h4> Objectives of establishing CSFTS </h4>

                            <p>The main objective of CSFTS is to enhance the capacities of support organization officials and farmer leaders to promote CSA technologies and practices. Subsequently, the project aims to impart their knowledge and skills on CSA practices to village-level potential farmers to respond to the adverse climate impacts in their villages.  In addition, the project mobilizes trained leader farmers to organize training activities and disseminating knowledge on CSA practices among peer groups. </p>


                            <h4> Operation Mechanism of CSFTS </h4>

                            <p>All training programs are conducted with the coordination of the extension division of the Department of Agriculture (DOA), Peradeniya, including providing resource persons for training sessions. Each batch consists of 30 participants, including leader farmers and technical officials from a support organization. The participants are identified by the provincial Deputy Project Director (DPD) offices to represent all Agrarian Service Center (ASC) divisions and make necessary arrangements to transport them to CSFTS premises on time. </p>

                            <p>All sessions are to be organized as residential (03 days) intensive, multi-session and multi-activity group learning sessions that use lecturing, group discussions, demonstrations of practical activities, videos, and other audio-visual resources as well as entertainment activities (drams and role play), etc. </p>


                            <h4> Technology Transfer Plan</h4>

                            <p>The technology transfer plan is developed by following 07 modules. Each module consists of thematic areas to be covered and lessons/topics to be discussed and learning objectives. The under-mentioned modules are used to transfer the CSA technologies to the participants. </p>

                             <ul>

                                <li>  Module (1) focuses on understanding climate change, its impacts, and ways of overcoming the risk of adverse impacts. </li>
                            </ul>

                            <p>This module defines climate change, climate change impacts, mitigation and adaptation of climate change impacts, and impacts of climate change on hotspot areas. </p>

                             <ul>

                                <li>     Module (2) introduces the concept of CSA. </li>
                            </ul>

                            <p>This module defines what is CSA, and how CSA can help to overcome the adverse impacts of climate change. </p>
                             <ul>

                                <li>      Module (3) focuses on managing local resources and landscape.  </li>
                            </ul>

                            <p>This module defines the sustainable management of the natural and farming landscape, building the resilience of local ecosystems and sustainable use of natural resources such as land, water, air, forest, and fisheries.  </p>
                             <ul>

                                <li>      Module (4) focuses on the on-farm use of CSA practices for sustainable intensification of production, increasing productivity, and minimizing the risk of adverse impacts.  </li>
                            </ul>

                            <p>This module defines the adjusting cropping calendar as per weather forecast, selection of weather-tulerated crop varieties, on-farm water management technics, building soil resilience through soil fertility management, protecting crops from climate-induced pest resurgence, and sustainable intensification of crop production, etc.  </p>
                             <ul>

                                <li>     Module (5) focuses on integrated crops, livestock, and fisheries management.</li>
                            </ul>

                            <p>This module defines the crop-livestock integration, use of Farm Yard Manure (FYM) to build soil fertility, fodder production and seasonal stocking of freshwater fish in the village tanks.  </p>
                             <ul>

                                <li>      Module (6) focuses on information mechanization, marketing, and value addition.  </li>
                            </ul>

                            <p>This module defines productivity enhancement through mechanization, the use of market information, value addition, and marketing for increasing farmer income.  </p>
                             <ul>

                                <li>      Module (7) is the wrap-up session.</li>
                            </ul>

                            <p>This module defines how the knowledge acquired from training will be used for the CSIAP interventions, how knowledge will be disseminated among fellow members, what roles to be played in Farmer Organizations (FOs), Producer Society (PSs), and Cascade Management Committees (CMCs), and post-evaluation of the training. </p>



                                <h4> Up to date progress of the CSFTS</h4>

                            <p>The CSFTS commenced its training since January 2023.The first training was given to the farmers in the Thirappane village. Subsequently a training of trainers (TOT) programme was conducted for the selected resource person group comprising the Agriculture instructors (AIs) & Subject Matter Officers (SMOs) from the DOA. Since 2023 January to 15th September 2024 have been conducted 86 training programs and trained 1942 farmer leaders on CSA technologies with the involvement of above said resource persons and scientist from the Field Crops Research and Development Institute (FCRDI) in Mahaillupallama, covering the farmers in 11 districts and 06 provinces.   </p>


                                <h4>Expected Outcome of the CSFTS</h4>

                            <p>CSIAP is originally planning to carry out 144 training sessions for leader farmers and technical officers during the project period and build capacities of 3600 participants on CSA technologies and practices. Hence, the expected outcome is to establish a CSA knowledge hub within the village and disseminate CSA technologies among beneficiaries residing in the hotspot areas covering all 47 ASCs divisions.  Subsequently, the CSIAP is planning to disseminate the CSA technology and practices to the whole farming communities, through the assistance of leader farmers by providing technical knowhow for minimum 10 farmers by a leader farmer. Eventually project is planning to disseminate CSA technology among 36000 farmers in Hotspot areas. </p>


                                <h4> Expected Outcome of the CSFTS</h4>

                            <p>The CSFTS is one of the most important assets for the farming community in Sri Lanka. Hence, ensuring its sustainability is important. As we mentioned earlier, all capacity-building programs will be conducted with the assistance of extension divisions and FCRDIâ€™s scientists in Mahailluppallama in DOA. Hence, leads to create a competent resource person pool in the DOA on CSA technologies and practices. Also, in future the training needs on CSA technologies will be increased due to the prevailing scenario. Meantime, Ministry of Agriculture (MOA) has already decided to commence new CSA  division within the MOA and to absorb this CSFTS into this division to  ensure sustainable functions of the center for the betterment of the vulnerable farming communities to build their resilience against the climate change impacts. </p>

