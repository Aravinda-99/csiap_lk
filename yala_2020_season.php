                            <p>This programme goes in line with the programme of the Implementation Ministry in promotion of food crops cultivation in the Yala 2020 season to overcome possible food shortage that could surface out owing to COVID 19 pandemic. This program focus on the promotion of Other Field Crops Cultivation following Climate Smart Agriculture Practices  in hot spot areas of 11 districts as from the food crops sector Other field crops occupies the highest share of importation at present. The total cultivable area expected to cover by the project is 4,029.4 Ac involving with more than 7,074 beneficiaries.</p>
                            <p>The main implementing agency of the project is the Provincial Department of Agriculture (PDoA) in all six provinces where the CSIAP is being implemented. The Agriculture Specialists together with the assistance of all other specialists of CSIAP will work closely with the PDoA in implementing the project obtaining necessary technical assistance through their field staff. The estimated total cost of the project is LKR M 392.8 .Farmers in the program are grouped into producer societies. The main purpose of forming a producer society is to synchronize farming activities and agronomic practices which are necessary to enhance the productivity and increase the profitability by reducing the cost of production, saving the usage of water, practice climate smart agriculture practices and strengthen the bargaining power of the farming community. The Producer Society is to function as a viable group of farmers to improve their production, productivity and post-harvest and marketing activities.</p>
