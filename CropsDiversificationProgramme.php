                            <p style="margin-top: 50px;">Crop diversification means the cultivation of drought tolerant non paddy crops in

paddy fields mainly during the Yala season. The CSIAP promote this Climate Smart

Agriculture (CSA) practice to efficiently utilize the available limited water resources

on productive war and obtain high returns from the farm by enhancing water

productivity. Crops diversification is practiced mainly during Yala season by diverting

the paddy cultivation lands into Other field Crops (OFC) cultivation (eg -Green gram,

Ground nut, Blak gram, Cowpea, Soya, Onion etc), those crops complete their

cropping cycle successfully, with low water quantity and give higher farm income

compared with paddy crop. Hence, this CSA practice leads to increased farm

productivity as well as water productivity with the contribution to the climate change

adaptation.</br>

Up to date project has carried out crop diversification programs in 6008.5 Ha with the

participation of 5858 project farmers. The crop diversification index is measured as a

percentage by comparing the non-paddy crops cultivation extent with the total

cultivation extent.</p>







                           







