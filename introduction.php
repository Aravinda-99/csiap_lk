                            <h3>Introduction</h3>
                            <p>Dry zone of Sri Lanka, being the most agriculturally productive region of the country often affects by the water shortage and has a long history of irrigation-based water management for agricultural production. The expansion of crop cultivation into the north, east, and southeast dry zones of the country was enabled through the construction of elaborate water management systems, dating back to the period between 500 BC and 300 AD. The systems are based on water capture in small reservoirs commonly known as ‘tanks’ connected through canals forming cascades. However, with the expansion of large-scale water storage and irrigation infrastructure these small tanks system in the dry zone of Sri Lanka was paid low attention. Nevertheless, with the emergence of water resource management to the forefront of the development agenda, the importance of small tank system management was highlighted. Recently, water scarcity has often been observed in the region as a result of inter-annual and inter-seasonal variability in rainfall, variations in catchment area and associated land use changes while reducing the proportion of rainfall available for capturing in downstream tanks despite the growing demand for water to meet the needs of the growing population.</p>
                            <p>Climate Smart Irrigated Agriculture Project (CSIAP), hence being developed to uplift the agriculture livelihood of the small-scale tank-based farming community living in the most climatically vulnerable farming areas of the country. The Project Development Objective is to improve the productivity and climate resilience of small holder agriculture sector in selected hotspot areas. To achieve the above development objective, the project will be implemented under four components. The project will be implemented over a period of six years (2018 – 2024).</p>
