                            <p style="margin-top: 50px;">Water management is a critical aspect of sustainable development, particularly in regions where

water resources are limited and subject to various pressures. The main objective of the project

is to improve the efficiency and productivity of the use of water harnessed by the cascade and

individual tanks to manage the demand for water for irrigated agriculture in the hotspots area

and to enhance the returns per unit volume of water and farm incomes.</p>



    <h4  style="margin-bottom: 3px;">Cascade Water Management Plan</h4>















<p>The hydrological assessments have been done for 11 river basins using the HEC-HMS

model. This Hydrological Analysis Report was used to estimate inflows for the Yala &amp;

Maha seasons of the cascades. Accordingly, average annual Inflows of the Cascades

were estimated by using the developed Rainfall-Runoff Model which is a developed

power equation. Finally, we have a rainfall-runoff relationship for each cascade. This

information is being used to prepare the cropping pattern of the cascade. To optimize

the cropping pattern of the cascade, a WEAP model was developed. Based on the

water availability ( reliability of Inflow and demand) and results of the economic

analysis, a suitable cropping pattern for each tank in the pilot cascades was prepared

and incorporated into the CMP for decision-making. Also, these hydrological studies

were applied to design the spillways of the tanks in the cascade for extreme weather

conditions and to prevent hole cascade.</p>



    <h4 style="margin-bottom: 3px;">Operation and Maintenance of Irrigation Systems</h4>















<p>Operation and Maintenance of Irrigation Systems aims to ensure the sustainable

operation and maintenance (O&amp;M) of tank systems at the individual tank level and

systems. Established Cascade Management Committees and strengthened Farmer

Organizations should work on O&amp;M of rehabilitated irrigation systems on sustainability

while a monitoring system for water use and availability will be designed and

implemented. All these activities are covered under the sub-component 2.2.</p>





<p>Accordingly, the sustainable operation &amp; maintenance and proper water management

of the village irrigation systems are essential to improve the water use efficiency in

these systems. The main key performance indicator for measuring irrigation system

Water Productivity is the water use efficiency, and the Project expects to increase the

water use efficiency of the village irrigation systems by 30% in the selected hotspot

area.</p>



<p>To achieve the target of water use efficiency, it is necessary to train the officers

involved in field-level management of Irrigation systems as well as the farmer

Organization. Also, required to prepare an O &amp; M plan and a Water Management Plan

for a particular crop season in the system during the training. The project already

trained officers in the Implementing Agency Staff &amp; Farmer leaders for water

management &amp; O and M activities in the irrigation systems. Also, prepared the O &amp; M

handbook for farmer training and a curriculum for TOT training of Implementing Agency

Staff. The rest of the Farmers in rehabilitated tank systems are being trained parallel to

the next cultivation seasons. Also, needed to complete the O &amp; M plan and water

management plan for each tank.</p>



<p>To ensure sustainable Water Management Practices in the cascade systems and

individual tanks, water issues from the sluice are to be measured by the appropriate

methods which should be handled easily by the Farmer Organization. Accordingly,

CSIAP provided a water measuring device, cutthroat flumes for 11 districts to use for

better water measuring of canal flows, and training was given to Farmer Leaders. Now,

farmers can handle their water issues accurately from the irrigation tanks without

interfering with Officials. Our final goal is the operation and maintenance of the tanks

sustainably handled by the farmers.</p>





<h4 style="margin-bottom: 3px;">Introducing an improved AWD apparatus for easy use by farmers</h4>



<p >Introducing the improved AWD (Alternate Wetting and Drying) apparatus designed for

easy use by farmers. This new device features a top indicator with a scale that is easily

readable from a distance, allowing farmers to quickly and accurately determine the

optimal time for irrigation. This innovation ensures efficient water management,

promoting sustainable farming practices and enhancing crop yield. Various models

were tested for suitability and the model given in Figure 1 was selected after carefully

considering all the aspects. It is planned to install 1000 nos. for the pilot tanks.</p>





<h4 style="margin-bottom: 3px;">Operation &amp; Maintenance can be explained at the individual and cascade levels

as follows</h4>



<h6 >Operation</h6>



<ul>

    <li> Tank water balance and determination of land Extent for Cultivation </li>

     <li> Cropping pattern &amp; Crop water Requirement for different Crops </li>

      <li> Application efficiency and irrigation efficiency for paddy and OFC</li>

       <li> Preparation of schedules for Rotational Water Issues </li>

        <li> Flow (Water) Measurement Methods for Minor Irrigation Systems</li>

         <li> Water Saving Techniques for Minor Irrigation Systems </li>

</ul>



<h6 >Maintenance</h6>



<ul>

    <li> Identify Irrigation components in the Village Irrigation system</li>

     <li> Importance of Maintenance and suitable methods</li>

      <li>Share (“Pangu”) List and Maintenance list</li>

       <li> Maintenance Estimate and Maintenance plan</li>

        <li> Maintenance Fund</li>

         <li> ” Kanna” meeting and Legal activities for maintenance works </li>

</ul>





<h6 >Methods followed for Saving Water</h6>



<ul>

    <li> Use appropriate O &amp; M methods</li>

     <li> Use Rainfall as much as possible for land preparation and crops</li>

      <li>Adopt dry-land preparation and dry-sowing method</li>

       <li> Starting Land preparation with the first Rainfall of the season</li>

        <li> Land leveling, Enlarge/regular shape of paddy land</li>

         <li> Apply short-term high-yield variety seed</li>

          <li> Land preparation in the system with all together</li>

           <li> Apply better agronomics practices</li>

            <li> Apply Alternative Wet and Dry (AWD)method for rotational water distribution</li>

</ul>





<h4 >Water Productivity/Water Use Efficiency (WUE) Measuring</h4>



<p >Measuring Water Use Efficiency (WUE) is the main key performance indicator under the

water management sub-component. Under this, it has been started to measure the

WUE effective from Yala season 2023 and is being continued using 48 pilot tanks

covering the project implementation provinces. The project target for the increase of

WUE by 30% and, with the calculations carried out until the 2023/2024 Maha season,

the average achievement was around a 22% increase of WUE.</p>







                           















