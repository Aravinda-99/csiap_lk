                            <p style="margin-top: 50px;">Micro irrigation is one of main Climate Smart Agriculture (CSA) strategy which leads

to increase both water productivity &amp; crop productivity using limited water resources

in climate affected areas hotspot areas. This practice uses to provide water as drops

or mist to the root zone of the plant by considering the crop water requirement. Drip,

Sprinkler, micro jet and rain horse irrigation systems are used by the CSIAP as micro

irrigation methods for crop production. Since this micro irrigation systems ensure the

constant water availability to the root zone, during the cropping cycle, it leads to

increase crop productivity by minimizing the water wastage and contribution to the

climate change adaptation.</p>







<p>During this period project has project has established micro irrigation facilities in

702.7 Ha by providing benefits for the 3423 farmers. Those farmers cultivate high

value crops such as chilies, onion, vegetables using these micro irrigation units with

Agro-wells.</p>







                           







