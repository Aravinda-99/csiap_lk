                            <p>Climate Smart Nutritional Sensitive Home Garden (CSNSHG) program aims to increase the nutrition level of the farming families by fulfilling the  food security of the household. This program implemented within the 11 districts by establishing  sustainable home gardens applying Climate Smart agriculture practices to ensure the continuous availability of vegetables, fruits yams and leafy vegetables at farmer households. It is proposed to establish 2695 home gardens in 11 districts with 4920 farm families. The estimated total cost of this 2nd program stage is  Rs; 221.73 million. The Climate Smart Home Garden Program is implemented only with the participation of women famers. It is expected that by this program women farmers are provided greater access to enhance  agriculture knowhow, participation in local cultivation programs  and decision making; they are also provided adequate representation at all levels in relevant planning and decision making which leads to empower the farmer women.</p>

