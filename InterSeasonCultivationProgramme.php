                            <p style="margin-top: 50px;">Inter-season cultivation is a most important Climate Smart Agriculture (CSA) practice

for the climate-affected farming community in hotspot areas which leads to receiving

additional income for the farmer family by utilizing available residual soil moisture in

the farm. This practice is defined as Zero tillage farming since this practice does not

involve any soil disturbance or ploughing and burning of crop residuals. This practice

helps to increase cropping intensity, land productivity and increase soil fertility since

we used to cultivate legume crops as inter-season crops under this programme. This

strategy directly contributes to the climate change adaptation and mitigation.</p>







