                            <h3> Organic Fertilizer Production Programme</h3>

                            <p>The Climate Smart Irrigated Agriculture Project (CSIAP) implemented under the Ministry of Agriculture has commenced a new program being aligned with the National Policy Frame Work ‘Vistas of Prosperity and Splendor’ to produce organic fertilizer in the hot spot areas of North Western, North, North Central, Eastern, Uva and Southern Provinces where the CSIAP is being implemented. This program is basically implemented with the coordination of  respective Provincial Departments of Agriculture and the Inter Provincial Departments of Agriculture.
This program is planned to be launched in selected Grama Niladhari Divisions of 47 Agrarian Service Regions in Kurunegala, Puttalam, Mullaitivu, Kilinochchi, Anuradhapura, Polonnaruwa, Trincomalee, Batticaloa, Ampara, Monaragala and Hambantota districts. The CSIAP will provide assistance to farmers by providing basic inputs needed for making solid compost and compost tea together with necessary training required for producing organic fertilizer.
.</p>



                            <p>Under this, measures will be taken to disseminate technical knowledge and provide with essential items required by farmers who produce organic fertilizer for their cultivation activities as well farmers and entrepreneurs who produce individual, small or medium scale organic fertilizer. Registration for organic fertilizer production with the Department of Agrarian Development is currently underway through Agriculture Instructors and Agricultural Research and Production Assistants.</p>



                            <p>Under the program the CSIAP has planned to produce 55,000 metric tons of solid compost and 8.0 million liters of compost tea. In addition, the CSIAP will join hands with the Department of Agriculture and Provincial Departments of Agriculture in developing six laboratories in Northern, Eastern, North Central, North Western, Uva and Southern Provinces to assess the quality of the compost.</p>



                            <p>The CSIAP will spend Rs 483.5 million with the financial assistance of the World Bank for this project.</p>



                            

